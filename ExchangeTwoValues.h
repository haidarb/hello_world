#ifndef UNTITLED_EXCHANGETWOVALUES_H
#define UNTITLED_EXCHANGETWOVALUES_H

#include<iostream>

using namespace std;

void swapAB() {

    int A, B, C;

    cout << " Please provide A: " << endl;
    cin >> A;
    cout << " Please provide B: " << endl;
    cin >> B;

    cout << " Brilliant !" << endl;

    C = A;
    A = B;
    B = C;

    cout << " Now our A is equal to " << A << endl;
    cout << ".. and B is equal to " << B << endl;



}

#endif //UNTITLED_EXCHANGETWOVALUES_H
